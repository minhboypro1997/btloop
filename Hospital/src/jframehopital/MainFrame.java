    private void bMoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bMoveActionPerformed
        BSdakhoa bs = new BSdakhoa();
        if(tMPK.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Chưa có bệnh nhân");
            
        }
        else{
            if(tCDSB.getText().isEmpty() || tCN.getText().isEmpty() || tM.getText().isEmpty() || tHA.getText().isEmpty() || tNT.getText().isEmpty() || tLDK.getText().isEmpty() || tTT.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Nhập thiếu dữ liệu của bệnh nhân");
                
        }
            else{
                String x = null;
                if(rXN.isSelected())
                    x = rXN.getText();
                if(rKhoa.isSelected())
                    x = cKhoa.getItemAt(cKhoa.getSelectedIndex());
            bs.addKQKDK(tMPK.getText(), Double.parseDouble(tCN.getText()), Double.parseDouble(tM.getText()), Double.parseDouble(tHA.getText()), Double.parseDouble(tNT.getText()), tLDK.getText(), tTT.getText(), tCDSB.getText(), x);
            
            db.connect();
            BN bn3 = db.selectBN(tMBNk.getText());
            queueBN(qtable3, bn3);
            Clear();
        }
        }
        
    }//GEN-LAST:event_bMoveActionPerformed

